/**
 * 判斷日期類型
 * - 補班日 → weekday（即使是週六）
 * - 國定假日 → holiday
 * - 週五/六/日 → weekend（甜品店旺日）
 * - 其餘 → weekday
 */
export function getDayType(dateStr: string): 'holiday' | 'weekend' | 'weekday' {
  // 1. 補班日 → weekday
  if (MAKEUP_WORK_DAYS.has(dateStr)) return 'weekday'

  // 2. 國定假日 → holiday
  const year = dateStr.slice(0, 4)
  if (HOLIDAYS[year]?.[dateStr]) return 'holiday'

  // 3. 週五/六/日 → weekend（甜品店假日旺日）
  const dow = new Date(dateStr + 'T00:00:00+08:00').getDay()
  if (dow === 5 || dow === 6 || dow === 0) return 'weekend'

  // 4. 其餘 → weekday
  return 'weekday'
}
